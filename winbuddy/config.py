"""
Конфигурация winbuddy.

Здесь живёт вся статика: какие пути безопасны, какие трогать НЕЛЬЗЯ ни при
каких условиях, какая модель используется и общие настройки безопасности.

Правило: агент никогда не решает сам, что можно, а что нельзя. Это решает
этот файл + слой безопасности. Модель только предлагает.
"""

from __future__ import annotations

import os
from pathlib import Path

# --------------------------------------------------------------------------
# Модель и провайдер
# --------------------------------------------------------------------------
# Основная рабочая лошадка агент-цикла. Дёшево и достаточно умно для tool use.
#
# ВАЖНО про имя модели: оно зависит от провайдера.
#   - официальный Anthropic:  "claude-sonnet-5"
#   - через aitunnel.ru:      "claude-sonnet-4.5" (их формат имён другой!)
# Задаётся через переменную окружения WINBUDDY_MODEL, чтобы не лезть в код.
MODEL = os.environ.get("WINBUDDY_MODEL", "claude-sonnet-4.5")

# Куда стучаться. Пусто => официальный api.anthropic.com. Для aitunnel:
#   ANTHROPIC_BASE_URL=https://api.aitunnel.ru
# SDK anthropic умеет работать через чужой base_url из коробки.
API_BASE_URL = os.environ.get("ANTHROPIC_BASE_URL", "").strip()

# Эскалация на тяжёлую модель (пока не используется).
MODEL_HEAVY = os.environ.get("WINBUDDY_MODEL_HEAVY", "claude-opus-4.7")

# Максимум шагов агента за одну задачу — предохранитель от бесконечного цикла.
MAX_AGENT_STEPS = 25

# --------------------------------------------------------------------------
# Безопасность путей
# --------------------------------------------------------------------------
# ЗАПРЕЩЁННЫЕ корни. Ни один инструмент (даже read) не трогает то, что лежит
# внутри этих путей. Это защита от "почисти диск C" -> снёс Windows.
#
# Пути в нижнем регистре, сравнение тоже в нижнем регистре (Windows
# регистронезависим по путям).
FORBIDDEN_ROOTS = [
    r"c:\windows",
    r"c:\program files",
    r"c:\program files (x86)",
    r"c:\programdata",
    # каталоги самого пользователя, которые опасно двигать целиком
    r"c:\users\default",
    r"c:\users\public",
]

# Имена папок, запрещённые на ЛЮБОМ диске (корзина, системные тома есть на
# каждом разделе — d:\$recycle.bin, e:\$recycle.bin и т.д.). Проверяются
# отдельно, потому что диск заранее неизвестен.
FORBIDDEN_ANY_DRIVE = [
    r"$recycle.bin",
    r"system volume information",
    r"$sysreset",
    r"recovery",
]

# РАЗРЕШЁННЫЕ корни для ЗАПИСИ (allowlist). Write-операции (перемещение,
# удаление, переименование) возможны ТОЛЬКО внутри них. Пустой список =>
# write запрещён везде (безопасный дефолт).
#
# Задаётся через .env, чтобы не лезть в код и не коммитить личные пути:
#   WINBUDDY_WRITE_ROOTS=D:\testdisk,C:\Users\Artem\Downloads
# Пути разделяются запятой.
def _parse_roots(env_value: str) -> list[str]:
    return [p.strip() for p in env_value.split(",") if p.strip()]

ALLOWED_WRITE_ROOTS: list[str] = _parse_roots(os.environ.get("WINBUDDY_WRITE_ROOTS", ""))

# Read-операции (анализ, отчёты) разрешены шире, но всё равно не в FORBIDDEN.
# Пустой список => разрешено читать где угодно, кроме FORBIDDEN.
ALLOWED_READ_ROOTS: list[str] = []

# --------------------------------------------------------------------------
# Поведение по умолчанию
# --------------------------------------------------------------------------
# dry-run: любая опасная операция сначала ТОЛЬКО показывает план, не выполняет.
DRY_RUN_DEFAULT = True

# Удаление всегда только в корзину, прямого удаления в коде быть не должно.
DELETE_TO_TRASH_ONLY = True

# --------------------------------------------------------------------------
# API-ключ (из переменной окружения, никогда не в коде)
# --------------------------------------------------------------------------
ANTHROPIC_API_KEY = os.environ.get("ANTHROPIC_API_KEY", "")

# --------------------------------------------------------------------------
# Пути проекта
# --------------------------------------------------------------------------
PROJECT_ROOT = Path(__file__).resolve().parent.parent
LOG_DIR = PROJECT_ROOT / "logs"
AUDIT_LOG = LOG_DIR / "audit.log"       # что агент РЕАЛЬНО сделал (write-действия)
APP_LOG = LOG_DIR / "winbuddy.log"      # обычные логи работы
